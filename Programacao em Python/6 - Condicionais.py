# Comparativos
"""
==  -> Igual a 
!=  -> Diferente de
>   -> Maior que
>=  -> Maior ou igual a
<   -> Menor que
<=  -> Menor ou igual a

Verificar mais de uma condicao
and -> E
or  -> OU
"""

# Condicional SE - if
if 10 < 5: 
    print("10 e menor que 5")

# Condicional SENAO - elif
elif 10 > 5:
    print("10 e maior que 5")

# Condicional SENAO - else
else:
    print("10 nao e maior ou menor que 5")